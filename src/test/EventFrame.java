    private void loginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginButtonMouseClicked
        // TODO add your handling code here:
        ArrayList adminLogins = new ArrayList();
        String username = usernameField.getText();
        char[] password = passwordField.getPassword();
        String passwordFull = new String(password);
        
        try {
            BufferedReader br = new BufferedReader(new FileReader("Admins.txt"));
            String ln = br.readLine();
            
            while (ln != null)
            {
                String [] split = ln.split(" ");
                adminLogins.add(split[0]);
                adminLogins.add(split[1]);
                ln = br.readLine();
                    
            }
            for (int i = 0; i < adminLogins.size(); i+=2)
            {
                int x = i + 1;
                if (adminLogins.get(i).equals(username) && adminLogins.get(x).equals(passwordFull))
                {
                    addButton.setVisible(true);
                    editButton.setVisible(true);
                    bookButton.setVisible(false);
                    modifyButton.setVisible(false);
                }
                
                if (usernameField.getText().equals("") || passwordField.getText().equals(""))
                {
                    addButton.setVisible(false);
                    editButton.setVisible(false);
                    bookButton.setVisible(true);
                    modifyButton.setVisible(true);
                }
            }
            br.close();
        }
        catch (IOException e) {
            System.out.println("Error");
        }
    }//GEN-LAST:event_loginButtonMouseClicked

