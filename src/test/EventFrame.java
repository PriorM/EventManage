    private void editdialogButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editdialogButtonMouseClicked
        // TODO add your handling code here:
        try         
        {
            //Recreates event with updated info
            BufferedWriter bw = new BufferedWriter(new FileWriter("Events.txt", true));
            
            String name = editnameField.getText();
            String type = edittypeField.getText();
            String capacity = editcapacityField.getText();
            String price = editpriceField.getText();
            String promotion = editpromotionField.getText();
            String date = editdateField.getText();
            String description = editdescriptionField.getText();
            
            bw.write(name + " " + type + " " + capacity + " " + price + " " + promotion + " " + date + " " + description);
            bw.close();
            
            //Deletes old event
            File inputFile = new File("Events.txt");
            File tempFile = new File("temp.txt");
            
            BufferedReader reader = new BufferedReader(new FileReader(inputFile));
            BufferedWriter writer = new BufferedWriter(new FileWriter(tempFile));
            
            String lineToRemove = eventList.getSelectedValue();
            String currentLine;
            
            while((currentLine = reader.readLine()) != null)
            {
                String trimmedLine = currentLine.trim();
                if(trimmedLine.equals(lineToRemove)) continue;
                writer.write(currentLine + System.getProperty("line.separator"));
            }
            writer.close();
            reader.close();
            
            BufferedReader treader = new BufferedReader(new FileReader(tempFile));
            BufferedWriter twriter = new BufferedWriter(new FileWriter(inputFile));
            
            String tcurrentLine;
                        
            while((tcurrentLine = treader.readLine()) != null)
            {
                String trimmedLine = tcurrentLine.trim();
                twriter.write(tcurrentLine + System.getProperty("line.separator"));
            }
            twriter.close();
            treader.close();
            
            
            
        }
        catch (IOException e)
        {
            System.out.println("Error");
        }
        editDialog.setVisible(false);
    }//GEN-LAST:event_editdialogButtonMouseClicked

